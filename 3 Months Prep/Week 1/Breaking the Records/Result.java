class Result {

    /*
     * Complete the 'breakingRecords' function below.
     *
     * The function is expected to return an INTEGER_ARRAY.
     * The function accepts INTEGER_ARRAY scores as parameter.
     */

    public static List<Integer> breakingRecords(List<Integer> scores) {
    // Write your code here
        ArrayList<Integer> recordCount = new ArrayList<Integer>(Arrays.asList(0, 0));
        
        ListIterator<Integer> it = scores.listIterator(1);
        int min = scores.get(0), max = scores.get(0);
        
        while(it.hasNext()){
            int score = it.next();
            if (score > max){
                recordCount.set(0, recordCount.get(0) + 1);
                max = score;
            }
            if (score < min){
                recordCount.set(1, recordCount.get(1) + 1);
                min = score;
            }
        }
        
        return recordCount;
    }

}

public class Solution {
    public static void main(String[] args) throws IOException {
        BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bufferedWriter = new BufferedWriter(new FileWriter(System.getenv("OUTPUT_PATH")));

        int n = Integer.parseInt(bufferedReader.readLine().trim());

        List<Integer> scores = Stream.of(bufferedReader.readLine().replaceAll("\\s+$", "").split(" "))
            .map(Integer::parseInt)
            .collect(toList());

        List<Integer> result = Result.breakingRecords(scores);

        bufferedWriter.write(
            result.stream()
                .map(Object::toString)
                .collect(joining(" "))
            + "\n"
        );

        bufferedReader.close();
        bufferedWriter.close();
    }
}
